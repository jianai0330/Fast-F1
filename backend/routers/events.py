from fastapi import APIRouter
from models.response import ok, err
import fastf1
import pandas as pd
import time

router = APIRouter()

# 内存缓存
_cache: dict = {}

def _cache_get(key: str, ttl: int):
    entry = _cache.get(key)
    if entry and time.time() - entry["ts"] < ttl:
        return entry["data"]
    return None

def _cache_set(key: str, data):
    _cache[key] = {"ts": time.time(), "data": data}

@router.get("")
def get_events(year: int = 2026):
    cache_key = f"events:{year}"
    cached = _cache_get(cache_key, 6 * 3600)
    if cached:
        return ok(cached)
    try:
        schedule = fastf1.get_event_schedule(year)
        events = []
        for _, row in schedule.iterrows():
            race_time_utc = None
            s5 = row.get("Session5Date", None)
            if s5 is not None and not pd.isna(s5):
                try:
                    ts = pd.Timestamp(s5)
                    ts = ts.tz_convert("UTC") if ts.tzinfo else ts.tz_localize("UTC")
                    race_time_utc = ts.isoformat()
                except Exception:
                    pass

            events.append({
                "round":          int(row.get("RoundNumber", 0)),
                "name":           row.get("EventName", ""),
                "country":        row.get("Country", ""),
                "location":       row.get("Location", ""),
                "date":           str(row.get("EventDate", ""))[:10],
                "format":         row.get("EventFormat", ""),
                "race_time_utc":  race_time_utc,
            })
        _cache_set(cache_key, events)
        return ok(events)
    except Exception as e:
        return err(str(e))


