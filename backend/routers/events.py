@router.get("/{round_num}/circuit")
def get_circuit_info(round_num: int, year: int = 2026):
    cache_key = f"circuit:{year}:{round_num}"
    cached = _cache_get(cache_key, 6 * 3600)
    if cached:
        return ok(cached)
    try:
        schedule = fastf1.get_event_schedule(year)
        row = schedule[schedule["RoundNumber"] == round_num]
        if row.empty:
            return err(f"Round {round_num} not found")
        location = row.iloc[0].get("Location", "")
        country = row.iloc[0].get("Country", "")
        event_name = row.iloc[0].get("EventName", "")

        info = CIRCUIT_INFO.get(location)
        result = {
            "location": location,
            "country": country,
            "event_name": event_name,
            "detail": info,
        }
        _cache_set(cache_key, result)
        return ok(result)
    except Exception as e:
        return err(str(e))
